     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\RedirectResponse
     */
    public function store(Request $request)
    {
        $img=($request->hasFile('img'))?$request->file('img')->store('Back/uploads'):'';
        if ($img==true) {
            $db = new Sliders([
                'name'=>$request->name,
                'price'=>$request->price,
                'img'=>$img,
            ]);
            $db->save();
            return redirect()->back();
        }

    }

